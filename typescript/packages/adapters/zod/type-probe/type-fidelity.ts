// ── step 1: generate probe fixture ────────────────────────────────

function generateFixture(): string {
	let output = `/**
 * Zod Type Probe Fixture - AUTO-GENERATED by type-fidelity harness
 * Do NOT edit manually.
 */
import { z } from "zod";

`;

	for (const probe of probeCases) {
		const result = convert({
			namespace: "probe",
			id: probe.name,
			varName: `probe_${probe.name}`,
			schema: probe.schema as object,
		});

		output += `// ${probe.description}\n`;
		output += `const probe_${probe.name} = ${result.schema};\n`;
		output += `type Probe_${probe.name} = ${result.type};\n\n`;
	}

	output += `export type {\n`;
	for (const probe of probeCases) {
		output += `  Probe_${probe.name},\n`;
	}
	output += `};\n`;

	return output;
}

// ── step 2: extract types via TS compiler API ─────────────────────

function hasAny(typeStr: string): boolean {
	return typeStr === "any" || typeStr.includes("any[]") || typeStr.includes("any,");
}

interface ProbeResult {
	name: string;
	type: string;
	hasAny: boolean;
}

function extractTypes(fixturePath: string): ProbeResult[] {
	const configPath = path.resolve(import.meta.dirname, "tsconfig.json");
	const configFile = ts.readConfigFile(configPath, ts.sys.readFile);
	const parsed = ts.parseJsonConfigFileContent(configFile.config, ts.sys, path.dirname(configPath));

	// override to use our fixture
	const program = ts.createProgram([fixturePath], parsed.options);
	const checker = program.getTypeChecker();

	const sourceFile = program.getSourceFile(fixturePath);
	if (!sourceFile) {
		console.error(`could not load ${fixturePath}`);
		process.exit(1);
	}

	const results: ProbeResult[] = [];
	ts.forEachChild(sourceFile, (node) => {
		if (ts.isTypeAliasDeclaration(node) && node.name.text.startsWith("Probe_")) {
			const type = checker.getTypeAtLocation(node);
			const typeStr = checker.typeToString(type, undefined, ts.TypeFormatFlags.NoTruncation);
			results.push({
				name: node.name.text,
				type: typeStr,
				hasAny: hasAny(typeStr),
			});
		}
	});

	return results;
}

// ── step 3: check expectations ────────────────────────────────────

interface CheckResult {
	probe: string;
	description: string;
	type: string;
	hasAny: boolean;
	expectAny: boolean;
	status: "pass" | "fail" | "improved";
}

function checkExpectations(extracted: ProbeResult[]): CheckResult[] {
	const typeMap = new Map(extracted.map((r) => [r.name, r]));
	const results: CheckResult[] = [];

	for (const probe of probeCases) {
		const probeName = `Probe_${probe.name}`;
		const ext = typeMap.get(probeName);
		if (!ext) {
			results.push({
				probe: probeName,
				description: probe.description,
				type: "(not found)",
				hasAny: true,
				expectAny: probe.expectAny,
				status: "fail",
			});
			continue;
		}

		let status: CheckResult["status"];
		if (!probe.expectAny && ext.hasAny) {
			// regression: was expected to be any-free but now has any
			status = "fail";
		} else if (probe.expectAny && !ext.hasAny) {
			// improved: was expected to have any but doesn't
			status = "improved";
		} else {
			status = "pass";
		}

		results.push({
			probe: probeName,
			description: probe.description,
			type: ext.type,
			hasAny: ext.hasAny,
			expectAny: probe.expectAny,
			status,
		});
	}

	return results;
}

// ── main ──────────────────────────────────────────────────────────

const fixturePath = path.resolve(import.meta.dirname, "probe-fixture.ts");
const fixtureContent = generateFixture();
await writeFile(fixturePath, fixtureContent, "utf8");

const extracted = extractTypes(fixturePath);
const results = checkExpectations(extracted);

const failures = results.filter((r) => r.status === "fail");
const improvements = results.filter((r) => r.status === "improved");
const anyCount = results.filter((r) => r.hasAny).length;

// report
console.log("zod type-fidelity report");
console.log("========================\n");

console.log("| probe | type | any? | expected | status |");
console.log("| ----- | ---- | ---- | -------- | ------ |");
for (const r of results) {
	const anyStr = r.hasAny ? "YES" : "no";
	const expectStr = r.expectAny ? "any ok" : "NO any";
	const icon = r.status === "fail" ? "FAIL" : r.status === "improved" ? "IMPROVED" : "ok";
	console.log(`| ${r.probe} | \`${r.type}\` | ${anyStr} | ${expectStr} | ${icon} |`);
}

console.log(`\ntotal: ${results.length} | any: ${anyCount} | any-free: ${results.length - anyCount}`);

if (improvements.length > 0) {
	console.log(`\n${improvements.length} probe(s) improved — update expectAny to false in type-fidelity.ts to lock in:`);
	for (const r of improvements) {
		console.log(`  - ${r.probe}: now \`${r.type}\``);
	}
}

if (failures.length > 0) {
	console.log(`\nFAILED: ${failures.length} probe(s) regressed to any:`);
	for (const r of failures) {
		console.log(`  - ${r.probe}: got \`${r.type}\`, expected no any`);
	}
	process.exit(1);
}

console.log("\nall checks passed");
